function action(mode, type, selection) {
    if (mode == 1) {
        status++;
    } else {
        status--;
    }
	if (cm.getInventory(1).isFull()) {
		cm.给物品(4280001,1);
		cm.给物品(5490001,1);
        cm.说明文字("请保证 #b装备栏#k 至少有2个位置。");
        cm.对话结束();
        return;
    } else if (cm.getInventory(2).isFull()) {
		cm.给物品(4280001,1);
		cm.给物品(5490001,1);
        cm.说明文字("请保证 #b消耗栏#k 至少有2个位置。");
        cm.对话结束();
        return;
    } else if (cm.getInventory(3).isFull()) {
		cm.给物品(4280001,1);
		cm.给物品(5490001,1);
        cm.说明文字("请保证 #b设置栏#k 至少有2个位置。");
        cm.对话结束();
        return;
    } else if (cm.getInventory(4).isFull()) {
		cm.给物品(4280001,1);
		cm.给物品(5490001,1);
        cm.说明文字("请保证 #b其他栏#k 至少有2个位置。");
        cm.对话结束();
        return;
    } else if (cm.getInventory(5).isFull()) {
		cm.给物品(4280001,1);
		cm.给物品(5490001,1);
        cm.说明文字("请保证 #b特殊栏#k 至少有2个位置。");
        cm.对话结束();
        return;
    }

	if (status == 0) {
        var chance = Math.floor(Math.random() * +1000);
        var finalitem = Array();
        for (var i = 0; i < itemList.length; i++) {
            if (itemList[i][1] >= chance) {
                finalitem.push(itemList[i]);
            }
        }
        if (finalitem.length != 0) {
            if (finalitem.length == 0) {
                return 1;
            }
            var item;
            var random = new java.util.Random();
            var finalchance = random.nextInt(finalitem.length);
            var itemId = finalitem[finalchance][0];
            var quantity = finalitem[finalchance][2];
            var notice = finalitem[finalchance][3];
            item = cm.gainGachaponItem3(itemId, quantity, "重生谜之蛋", notice);
            if (item != -1) {
				cm.说明文字("恭喜你获得了 "+cm.显示物品(itemId)+" x 1");
            } else {
                cm.playerMessage(1,"请你确认在背包的装备，消耗，其他窗口中是否有一格以上的空间。");
            }
            cm.safeDispose();
        } else {
            cm.playerMessage(1,"今天的运气可真差，什么都没有拿到。");
            cm.safeDispose();
        }
        cm.对话结束();
    }
}